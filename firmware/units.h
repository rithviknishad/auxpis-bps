/*
    Units definition
*/

#define unit
#define milli       * (1e-3)

#define second      unit
#define ms          milli second
#define minute      * 60 second
#define hour        * 60 minute

#define Volt        unit
#define V           Volt
#define mV          milli Volt

#define Ampere      unit
#define A           Ampere
#define mA          milli Ampere

#define Watt        unit
#define W           Watt
#define mW          milli Watt

#define Wh          Watt hour
#define mWh         milli Wh